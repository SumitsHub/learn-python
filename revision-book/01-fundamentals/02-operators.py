##* Operators used in Python

##* Arithmetic Operators
num1 = 10
num2 = 20

## '+' - addition
print(num1 + num2)  # Output: 30

## '-' - subtraction
print(num1 - num2)  # Output: -10

## '*' - multiplication
print(num1 * num2)  # Output: 200

## '/' - float division
print(num2 / num1)  # Output: 2.0

## '//' - integer division
print(num2 // num1)  # Output: 2

## '%' - modulus operator or reminder operator
print(num2 % num1)  # Output: 0

## '**' - exponent
print(num1 ** 2)  # Output: 100


##* Rounding off values using 'round' function

print(round(2**0.5, 4))  # Output: 1.4142